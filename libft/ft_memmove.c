#include "libft.h"

void	*ft_memmove(void *dest, const void *source, size_t n)
{
	char	*s;
	char	*d;
	size_t	i;

	s = (char *)source;
	d = (char *)dest;
	if (d > s)
	{
		while (n--)
			d[n] = s[n];
	}
	else
	{
		i = 0;
		while (i < n)
		{
			d[i] = s[i];
			i++;
		}
	}
	return (d);
}
